function soma_exercicio16(numero1, numero2) {
    return numero1 + numero2;
}

function soma_exercicio17(numero1, numero2) {
    if (numero1 === '') {
        return 0;
    } else {
        if (numero2 === '') {
            return 0;
        } else {
            return numero1 + numero2;
        }
    }
}